using demo.Data;
using demo.Models;
using Microsoft.EntityFrameworkCore;


var builder = WebApplication.CreateBuilder(args);

// Add services to the container.

builder.Services.AddControllers();
// Learn more about configuring Swagger/OpenAPI at https://aka.ms/aspnetcore/swashbuckle
builder.Services.AddEndpointsApiExplorer();
builder.Services.AddSwaggerGen();
//builder.Services.AddDbContext<ContextApplication>(options => options.UseInMemoryDatabase("TrialsDB"));
//builder.Services.AddAutoMapper(ContextApplication,add);
builder.Services.AddDbContext<ContextApplication>(options =>
        options.UseSqlServer(builder.Configuration.GetConnectionString("TrialsApiConnectionString")));

var app = builder.Build();

// Configure the HTTP request pipeline.
if (app.Environment.IsDevelopment())
{
    app.UseSwagger();
    app.UseSwaggerUI();
}

app.UseHttpsRedirection();

app.UseAuthorization();

app.MapControllers();

app.Run();
